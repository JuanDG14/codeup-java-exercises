public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\nWe don't need no thought control";
        System.out.println(education);
        System.out.println("");
        String quotations = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotations);
        System.out.println("");
        String driveString = "In windows, the main drive is usually C:\\";
        System.out.println(driveString);
        System.out.println("");
        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslash);
        System.out.println("");

    }
}
